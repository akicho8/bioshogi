require "./beauty_minimax"

class NegaMax < BeautyMinimax
  def compute_score(*args)
    nega_max(*args)
  end

  def nega_max(turn:, depth_max:, depth: 0)
    player = mediator.player_at(turn)

    # 一番深い局面に達したらはじめて評価する
    if depth >= depth_max
      return [mediator.evaluate(player), []] # ミニマックスのときとは異なり player から見たscore
    end

    # 合法手がない場合はパスして相手に手番を渡す
    children = mediator.can_put_points(player)
    if children.empty?
      score, before_readout = nega_max(turn: turn + 1, depth_max: depth_max, depth: depth + 1)
      return [-score, [:pass, *before_readout]]
    end

    max = -Float::INFINITY
    readout = []

    children.each do |point|
      mediator.put_on(player, point) do
        score, before_readout = nega_max(turn: turn + 1, depth_max: depth_max, depth: depth + 1)
        score = -score # 相手の一番良い手は自分の一番悪い手としたいので符号を反転する
        if score > max
          readout = [point, *before_readout]
          max = score
        end
      end
    end

    [max, readout]
  end
end

if $0 == __FILE__
  NegaMax.new.run               # => {:o=>1, :x=>10}
end
