# ミニマックスの綺麗な実装

require "./dirty_minimax"

class BeautyMinimax < DirtyMinimax
  def mini_max(turn:, depth_max:, depth: 0)
    # 一番深い局面に達したらはじめて評価する
    if depth >= depth_max
      return [mediator.evaluate(:o), []] # 常に「先手から」の評価値
    end

    # 合法手がない場合はパスして相手に手番を渡す
    player = mediator.player_at(turn)
    children = mediator.can_put_points(player)
    if children.empty?
      score, before_readout = mini_max(turn: turn + 1, depth_max: depth_max, depth: depth + 1)
      return [score, [:pass, *before_readout]]
    end

    if turn.even?
      max = -Float::INFINITY
    else
      max = Float::INFINITY
    end

    readout = []
    children.each do |point|
      mediator.put_on(player, point) do
        score, before_readout = mini_max(turn: turn + 1, depth_max: depth_max, depth: depth + 1)
        if turn.even?
          # 自分が自分にとってもっとも有利な手を選択する
          flag = score > max
        else
          # 相手が自分にとってもっとも不利な手を選択する
          flag = score < max
        end
        if flag
          readout = [point, *before_readout]
          max = score
        end
      end
    end

    [max, readout]
  end
end

if $0 == __FILE__
  BeautyMinimax.new.run         # => {:o=>1, :x=>10}
end
